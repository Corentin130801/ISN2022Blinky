package game;

public class Character {
	// Attributs
	
	private int health;
	
	// Constructeurs
	
	public Character(int h) {
		health = h;
		
	}
	
	// Accesseurs
	
	public int getHealth() {
		return health;
	}
	
	// Setters
	
	public void setHealth(int h) {
		health = h;
		
	}
	
	// Attaque
	
	public int Attack(int a) {
		return a;
		
	}

}
